def translate_diagnostic_field(language: str | None, code: str, field: str, value: str) -> str:
    if normalize_language(language) != "zh":
        return value
    if field in {"title", "recommendation"}:
        mapped = DIAGNOSTIC_FIELD_ZH.get(str(code), {}).get(field)
        if mapped:
            return mapped
    return translate_warning(language, value)


